#include "3-calc.h"
/**
 * op_add - calc operations
 * @a: int
 * @b: int
 * Return: given operation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function
 * @a: int
 * @b: int
 * Return: given
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - contains func
 * @a: int
 * @b: int
 * Return: given ops
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - contains func
 * @a: int
 * @b: int
 * Return: given ops
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - contains func
 * @a: int
 * @b: int
 * Return: given ops
 */
int op_mod(int a, int b)
{
	return (a % b);
}
